public class GallonsToLiters {
    public static void main(String[] args) {
        double gallon = 15;

        double liter = gallon * 3.785;
        System.out.println((int)liter);


        double liter1 = 50;
        double gallon1 = liter1 * 3.785;
        System.out.println(gallon1);
    }
}
